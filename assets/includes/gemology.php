  function rings(){
 var pos = $('.center-gem').position(),
 radiusSat = $('.sat1').width() * 1,
 radius = $('.center-gem').width() * 1,
 cx = pos.left + radius,
 cy = pos.top + radius,
 x, y, angle = 0, angles = [],
 spc = 360 / 8,
 deg2rad = Math.PI / 180,
 i = 0;

 for(;i < 9; i++) {
  angles.push(angle);
  angle += spc;
}

/// space out radius
radius += (radiusSat + 10);
loop();        

  function loop() {


    for(var i = 0; i < angles.length; i++) {

      angle = angles[i];

      x = cx + radius * Math.cos(angle * deg2rad);
      y = cy + radius * Math.sin(angle * deg2rad);

      $('.sat' + i).css({left:x - radiusSat, top:y - radiusSat});

      angles[i] = angles[i] + 1;
      if (angles[i] > 360) angles[i] = 0; 


    }   

  window.requestAnimationFrame(loop);


}

}

$('#g_dob').datepicker({
  dateFormat:'yy-mm-dd',
  changeMonth:true,
  changeYear:true,
  yearRange:'-40:5-',
  onSelect : function(){
    $('#g_dob').val(this.value);                  

    function calculateAge(birthday) {
      var now = new Date();
      var past = new Date(birthday);
      var nowYear = now.getFullYear();
      var pastYear = past.getFullYear();
      var age = nowYear - pastYear;

      return age;
    };

    $('#g_age').val(calculateAge(this.value));
  }
});
</script>